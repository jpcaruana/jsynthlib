package core;
import java.io.*;

//----------------------------------------------------------------------------------------------------------------------
// Class: NameValue
//----------------------------------------------------------------------------------------------------------------------

public class NameValue extends Object implements Serializable {

  //----- Instance variables
  protected String sName;
  protected int value;

//----------------------------------------------------------------------------------------------------------------------
// Class Constructor: NameValue(String, int)
//----------------------------------------------------------------------------------------------------------------------

  public NameValue(String sName, int value) {
    this.setName(sName);
    this.setValue(value);
  }

//----------------------------------------------------------------------------------------------------------------------
// NameValue->getName
//----------------------------------------------------------------------------------------------------------------------

  public String getName() {
    return this.sName;
  }

//----------------------------------------------------------------------------------------------------------------------
// NameValue->setName
//----------------------------------------------------------------------------------------------------------------------

  public void setName(String sName) {
    this.sName = sName;
  }

//----------------------------------------------------------------------------------------------------------------------
// ValueValue->getValue
//----------------------------------------------------------------------------------------------------------------------

  public int getValue() {
    return this.value;
  }

//----------------------------------------------------------------------------------------------------------------------
// ValueValue->setValue
//----------------------------------------------------------------------------------------------------------------------

  public void setValue(int value) {
    this.value = value;
  }
} // End Class: NameValue